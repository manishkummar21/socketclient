package com.vogo;


public class Result {

    private String vehicleID;

    private boolean helmetdata;

    public Result() {
    }

    public String getVehicleID() {
        return vehicleID;
    }

    public void setVehicleID(String vehicleID) {
        this.vehicleID = vehicleID;
    }

    public boolean isHelmetdata() {
        return helmetdata;
    }

    public void setHelmetdata(boolean helmetdata) {
        this.helmetdata = helmetdata;
    }
}
